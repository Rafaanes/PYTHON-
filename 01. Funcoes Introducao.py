'''
FUNÇÃO -> é um bloco de código reutilizável
    
    Vantagem: organização    

    -> def o que significa?
    =======================
    É uma palavra-chave seguida pelo 
    nome da função entre parênteses
    
Modularidade: Funções devem ser criadas 
para realizar uma tarefa específica. 
Se a sua função estiver longa e complexa, 
o ideal é dividi-la em sub-funções para 
melhorar a legibilidade e manutenção do código.


 def = definir 
 =============
 precisamos definir o que a função precisa receber
 como informação dentro dos parenteses
 Obs.: se houver a necessidade de informações dentro
 dos parenteses ficaria(preco, aliquota1, aliquota2 e etc...)

  return =  é usado para retornar um valor de 
            uma função e encerrá-la. Caso não seja
            declarado um valor de retorno, 
            a função retorna o 
            valor None (que significa nada, sem valor).

  FUNÇÃO quando usar ?
  Resposta: quando existir uma lógica separada 
  do resto do programa

  Resumo:
  =======
  def <nome_função> (<definição dos parâmetros >) :
      <Bloco de comandos da função>
      <retornar valor>

'''









