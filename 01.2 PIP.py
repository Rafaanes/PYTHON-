'''

PIP GERENCIAMENTO DE PACOTES
============================

"pip" significa "Pip Installs Packages" (ou "Pip Instala Pacotes", em português). 
Ele é o sistema de gerenciamento de pacotes padrão para Python, onde você
instala bibliotecas de terceiros!
        ------------------------

1) Instalar Pacotes: Comando pip install nome_do_pacote

2) Atualizar Pacotes: Comando pip install --upgrade nome_do_pacote

3) Desinstalar Pacotes: Comando pip uninstall nome_do_pacote

4) Listar Pacotes Instalados: Comando pip list para listar todos os pacotes 
   instalados em seu ambiente Python

5) biblioteca não está instalada: WARNING: Package(s) not found

'''

