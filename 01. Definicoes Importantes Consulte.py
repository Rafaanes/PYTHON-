# 2ª Informação - VARIÁVEIS
'''
    Variáveis(resumo):
    =================
    Espaço na memória resevado para armazenar dados.
    Não pode ser uma palavra reservada da liguagem.
    Não pode conter espaço, para separarmos usamos _ (underline)
    Pode ser usado padrões principalmente o CamelCase

    Regras para Nomear Variáveis
    ============================
    São basicamente 4 regras que você deve seguir:

    1) o nome não deve conter espaços
    2) o nome não deve começar com um número
    3) só é aceito o símbolo _ (underline ou underscore); outros símbolos e caracteres especiais não são aceitos
    4) não usar as palavras-chave (palavras reservadas) do Python

    Declare variáveis conforme exemplos:
    ===================================

    Inválidos: meu email, meu-email, 1meuemail, Meu Email, meuemail*, MEU&EMAIL

    Válidos: meuemail, meu_email, MeuEmail, Meu_email, MEUEMAIL
    
    O que são variáveis na programação(detalhado)?
    ==============================================
    Já as variáveis são os componentes que guardam esses valores na 
    memória do computador. Por exemplo, você pode criar uma variável
    chamada idade e atribuir um valor numérico a ela.

    O ato de colocar um valor a uma variável é chamado de atribuição. 
    No Python fazemos isso usando 
    o símbolo de = (um único símbolo de igual). 

    Compreendendo melhor
    ====================
    Valor: representam números, palavras, textos, verdadeiro ou falso.
    Variável: componente que guarda valores na memória do computador.
    Atribuição: Ação de dar um valor a uma variável.
    Como atribuir valores à variáveis no Python: usa = (símbolo de 
    igualdade único). Assim que o valor é atribuído a ela, a variável 
    é declarada.
    Precisa declarar o tipo de dado no Python? Não é necessário. 
    Python é de tipagem dinâmica. É escolhido dinamicamente,
    podendo ser alterado durante compilação ou execução do programa


'''

